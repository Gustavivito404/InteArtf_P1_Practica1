# Declaracion de variables y asignacion de valores
# Tipos de datos basicos en Python
mi_variable = 10          # Variable entera
mi_variable_flotante = 3.14  # Variable de punto flotante
mi_variable_cadena = "Gustavo"  # Variable de cadena de texto
mi_variable_booleana = True  # Variable booleana

print("Valor de mi_variable (entero):", mi_variable)
print("Valor de mi_variable_flotante (float):", mi_variable_flotante)
print("Valor de mi_variable_cadena (string):", mi_variable_cadena)
print("Valor de mi_variable_booleana (bool):", mi_variable_booleana)

# Ahora provemos a cambiar los valores de las variables
mi_variable = 20
mi_variable_flotante = 2.71
mi_variable_cadena = "Juan"
mi_variable_booleana = False

print("Nuevo valor de mi_variable (entero):", mi_variable)
print("Nuevo valor de mi_variable_flotante (float):", mi_variable_flotante)
print("Nuevo valor de mi_variable_cadena (string):", mi_variable_cadena)
print("Nuevo valor de mi_variable_booleana (bool):", mi_variable_booleana)